DATA = {
    'omlet': {
        'яйца, шт': 2,
        'молоко, л': 0.1,
        'соль, ч.л.': 0.5,
    },
    'pasta': {
        'макароны, г': 0.3,
        'сыр, г': 0.05,
    },
    'buter': {
        'хлеб, ломтик': 1,
        'колбаса, ломтик': 1,
        'сыр, ломтик': 1,
        'помидор, ломтик': 1,
    },
    # можете добавить свои рецепты ;)
}

def print_recipe(request, name):
    par = request.GET.get("serving",1)
    d= {i:val*int(par) for i,val in DATA[name].items()}
    context = {'recipe': d, 'dish':name, 'number':par} 
    return render(request, 'calculator/index.html', context)

